<?php

namespace App\Enums;

enum Status: string
{
    case Open = 'open';
    case InProgress = 'in-progress'; 
    case Resolved = 'resolved';
    case Closed = 'closed';

    public function label(): string
    {
        return match ($this) {
            self::Open => 'Open',
            self::InProgress => 'In Progress',
            self::Resolved => 'Resolved',
            self::Closed => 'Closed',
        };
    }
}